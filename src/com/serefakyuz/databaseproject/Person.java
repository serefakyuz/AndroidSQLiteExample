package com.serefakyuz.databaseproject;
/**
 * 
 * @author serefa
 *	The model class for Person Objects
 */
public class Person {
	private String id;
	private String name;
	private String lastName;
	private String age;
	
	/**
	 * 
	 * getters, setters
	 */
	public String getID() {
		return id;
	}
	public void setID(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
